fun main_variaveis(){
    // declaração de variáveis
    /**
     * VAR - valor mutável, CamelCase:
     * variável que pode ter seu valor alterado durante o código
     *
     * VAL - valor imutável, camelCase:
     * variável qie terá somente o valor atribuído (similar ao final em java)
     *
     * CONST VAL - valor imutável, SNALE_CASE:
     * Constante cujo valor é atribuído durante a compilação
     */

    // exemplos
    var currentAge = 27;
    var currentAge2:Int?
    currentAge2 = 28;

    val currentAge3 = 27;
    val currentAge4:Int?
    currentAge2 = 27;

    /* Uma variável não pode ser declarada sem tipo e sem atribuição
    * e uma variável com inferência de tipo só receberá valores do mesmo tipo que a sua primeira atribuição.*/




